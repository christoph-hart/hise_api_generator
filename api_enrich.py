def generate_class_markdown(class_name: str, c: dict,
                            test_results: dict = None) -> str:
    """Generate a clean markdown page for one class (web/userDocs mode only).

    This produces the same content and ordering as the HTML web preview
    but in plain markdown suitable for docs.hise.dev or other consumers.
    Structured fields (valueDescriptions, callbackProperties) are omitted --
    they exist for machine consumption only.
    """
    if test_results is None:
        test_results = {}
    desc = c.get("description", {})
    methods = c.get("methods", {})
    lines = []

    # Header
    category = c.get("category", "")
    lines.append(f"# {class_name}")
    if category:
        lines.append(f"*{category}*")
    lines.append("")
    if desc.get("brief"):
        lines.append(desc["brief"])
        lines.append("")

    # Class-level userDocs
    if desc.get("userDocs"):
        lines.append("## Overview")
        lines.append("")
        lines.append(desc["userDocs"].strip())
        lines.append("")

    # Code example
    if desc.get("codeExample"):
        lines.append("## Usage Example")
        lines.append("")
        lines.append("```javascript")
        lines.append(desc["codeExample"].strip())
        lines.append("```")
        lines.append("")

    # Common mistakes: now part of class userDocs (authored in Phase 4a Readme.md)
    # The userDocs text above already contains the ## Common Mistakes section if present.
    # Structured commonMistakes data remains in the JSON for review mode and MCP consumers.

    # Methods
    if methods:
        lines.append("## Methods")
        lines.append("")

        for name, m in methods.items():
            # Skip disabled methods in clean markdown output
            if m.get("disabled"):
                continue

            sig = format_signature(class_name, name, m)
            lines.append(f"### `{sig}`")
            lines.append("")

            # Minimal example (right after signature)
            if m.get("minimalExample"):
                lines.append(f"`{m['minimalExample']}`")
                lines.append("")

            # Parameter table (right after signature)
            if m.get("parameters"):
                lines.append("| Parameter | Type | Description |")
                lines.append("|-----------|------|-------------|")
                for p in m["parameters"]:
                    pt = display_type(p.get("type", "var")) or "var"
                    lines.append(
                        f"| `{p.get('name', '')}` "
                        f"| `{pt}` "
                        f"| {p.get('description', '')} |"
                    )
                lines.append("")

            # userDocs prose (or fall back to base description)
            if m.get("userDocs"):
                lines.append(m["userDocs"].strip())
                lines.append("")
            elif m.get("description"):
                lines.append(m["description"])
                lines.append("")

            # Pitfalls: now part of method userDocs (authored in Phase 4a .md files)
            # The userDocs text above already contains > **Warning:** blockquotes if present.
            # Structured pitfall data remains in the JSON for review mode and MCP consumers.

            # Examples
            for ex in m.get("examples", []):
                slug = ex.get("slug", "")
                source = ex.get("source", "auto")
                header = desluggify(slug) if slug else (ex.get("title", "") or "Example")

                # Validation status from sidecar
                badge = ""
                if slug and test_results:
                    key = f"{class_name}.{name}.{source}.{slug}"
                    result = test_results.get(key)
                    if result:
                        if result.get("tested") and result.get("passed"):
                            badge = " (validated)"
                        elif result.get("skipped"):
                            badge = " (skipped)"

                lines.append(f"#### {header}{badge}")
                lines.append("")
                fence_lang = "hsc" if ex.get("format") == "hsc" else "javascript"
                lines.append(f"```{fence_lang}")
                lines.append(ex.get("code", "").strip())
                lines.append("```")
                lines.append("")

            # Cross references
            if m.get("crossReferences"):
                refs = ", ".join(m["crossReferences"])
                lines.append(f"**See also:** {refs}")
                lines.append("")

            # Property system links
            pl = m.get("propertyLinks", {})
            eq_links = pl.get("equivalent", []) if isinstance(pl, dict) else []
            rel_links = pl.get("related", []) if isinstance(pl, dict) else []
            if eq_links or rel_links:
                lines.append("**Property system:**")
                if eq_links:
                    lines.append(f"- Equivalent: {', '.join(eq_links)}")
                if rel_links:
                    lines.append(f"- Related: {', '.join(rel_links)}")
                lines.append("")

            # Interaction notes
            if m.get("interactionNotes"):
                lines.append("**Interaction notes:**")
                for n in m.get("interactionNotes", []):
                    lines.append(f"- {n}")
                lines.append("")

            lines.append("---")
            lines.append("")

    return "\n".join(lines)


