def strip_heading_section(text: str, heading: str) -> str:
    """Remove a ## heading and its content from markdown text.

    Strips from '## <heading>' (case-insensitive) up to the next '## ' heading
    or end of string. Returns the text with that section removed.
    """
    import re
    pattern = re.compile(
        r'(?m)^## ' + re.escape(heading) + r'\s*\n'  # the heading line
        r'(.*?)'                                       # content (non-greedy)
        r'(?=^## |\Z)',                                # next heading or end
        re.DOTALL | re.IGNORECASE
    )
    return pattern.sub('', text).strip()


def desluggify(slug: str) -> str:
    """Convert a URL slug to a human-readable title.

    'validating-preprocessor-definitions-at' -> 'Validating Preprocessor Definitions At'
    'methods-1' -> 'Methods 1'
    'basic' -> 'Basic'
    """
    return slug.replace("-", " ").title()


def md_inline(text: str) -> str:
    """HTML-escape text, then convert inline markdown to HTML tags.

    Handles: `code`, **bold**, *italic*, ![alt](src) images.
    """
    text = html_escape(text)
    text = re.sub(r"`([^`]+)`", r"<code>\1</code>", text)
    # Bold (**text**) -- must come before italic
    text = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", text)
    # Italic (*text*) -- single asterisk, not inside bold
    text = re.sub(r"\*([^*]+)\*", r"<em>\1</em>", text)
    # Inline images ![alt](src) -- SVG diagrams get a diagram-{id} anchor
    def _img_replace(m):
        alt, src = m.group(1), m.group(2)
        img_tag = f'<img src="images/{src}" alt="{alt}" style="max-width:100%;">'
        # SVG diagrams: extract id from {type}_{id}.svg pattern
        svg_match = re.match(r"[a-z]+_(.+)\.svg$", src)
        if svg_match:
            diagram_id = svg_match.group(1)
            return f'<div id="diagram-{diagram_id}" style="margin:16px 0;">{img_tag}</div>'
        return img_tag
    text = re.sub(r"!\[([^\]]*)\]\(([^)]+)\)", _img_replace, text)
    # Inline links [text](url)
    text = re.sub(r"\[([^\]]+)\]\(([^)]+)\)", r'<a href="\2">\1</a>', text)
    return text


def _md_table_to_html(block: str) -> str:
    """Convert a markdown pipe table block to an HTML <table>.

    Returns empty string if the block is not a valid markdown table.
    """
    lines = [l.strip() for l in block.strip().splitlines() if l.strip()]
    if len(lines) < 2:
        return ""
    # All lines must start with '|'
    if not all(l.startswith("|") for l in lines):
        return ""
    # Second line must be the separator (contains only |, -, :, spaces)
    if not re.match(r"^[\s|:\-]+$", lines[1]):
        return ""

    def split_row(line: str) -> list:
        cells = line.strip("|").split("|")
        return [c.strip() for c in cells]

    headers = split_row(lines[0])
    html = '<table class="md-table">\n<thead><tr>'
    for h in headers:
        html += f"<th>{md_inline(h)}</th>"
    html += "</tr></thead>\n<tbody>\n"
    for row_line in lines[2:]:
        cells = split_row(row_line)
        html += "<tr>"
        for cell in cells:
            html += f"<td>{md_inline(cell)}</td>"
        html += "</tr>\n"
    html += "</tbody></table>\n"
    return html


def md_split_blocks(text: str) -> list:
    """Split markdown text into blocks at blank lines, respecting code fences.
    
    Unlike a naive split("\\n\\n"), this keeps code blocks with internal blank
    lines as a single block.
    """
    blocks = []
    current = []
    in_fence = False
    
    for line in text.split("\n"):
        if line.strip().startswith("```"):
            if not in_fence:
                in_fence = True
            else:
                in_fence = False
            current.append(line)
        elif not in_fence and line.strip() == "" and current:
            # Blank line outside a code fence - flush the current block
            block_text = "\n".join(current).strip()
            if block_text:
                blocks.append(block_text)
            current = []
        else:
            current.append(line)
    
    # Flush remaining
    block_text = "\n".join(current).strip()
    if block_text:
        blocks.append(block_text)
    
    return blocks


def md_block(block: str) -> str:
    """Render a markdown block as HTML -- table, heading, list, code fence, or paragraph."""
    block = block.strip()
    if not block:
        return ""

    # Code fence (```...```)
    if block.startswith("```"):
        lines = block.split("\n")
        lang_match = re.match(r"^```(\w+)?", lines[0])
        lang = lang_match.group(1) if lang_match and lang_match.group(1) else "javascript"
        # Find closing fence
        code_lines = []
        for line in lines[1:]:
            if line.strip() == "```":
                break
            code_lines.append(line)
        code_text = html_escape("\n".join(code_lines))
        return f'<pre><code class="language-{lang}">{code_text}</code></pre>\n'

    # Table
    table_html = _md_table_to_html(block)
    if table_html:
        return table_html

    lines = block.split("\n")

    # Heading (###, ##, #)
    if lines[0].startswith("#"):
        heading_match = re.match(r"^(#{1,6})\s+(.*)", lines[0])
        if heading_match:
            level = len(heading_match.group(1))
            text = heading_match.group(2)
            html = f"<h{level}>{md_inline(text)}</h{level}>\n"
            # If there are more lines after the heading, render them too
            rest = "\n".join(lines[1:]).strip()
            if rest:
                html += md_block(rest)
            return html

    # Blockquote (lines starting with "> ")
    if all(l.startswith("> ") or l.startswith(">") or l == "" for l in lines) and any(l.startswith(">") for l in lines):
        inner_lines = []
        for l in lines:
            if l.startswith("> "):
                inner_lines.append(l[2:])
            elif l.startswith(">"):
                inner_lines.append(l[1:])
            else:
                inner_lines.append(l)
        inner_html = md_inline(" ".join(ln for ln in inner_lines if ln.strip()))
        return f'<blockquote class="warning">{inner_html}</blockquote>\n'

    # Bullet list (lines starting with "- ")
    if all(l.startswith("- ") or l.startswith("  ") or l == "" for l in lines) and any(l.startswith("- ") for l in lines):
        items: list[str] = []
        for line in lines:
            stripped = line.strip()
            if stripped.startswith("- "):
                items.append(md_inline(stripped[2:]))
            elif stripped and items:
                # Continuation line: append to current item with <br>
                items[-1] += f"<br>{md_inline(stripped)}"
        html = "<ul>\n"
        for item in items:
            html += f"<li>{item}</li>\n"
        html += "</ul>\n"
        return html

    # Ordered list (lines starting with "1. ", "2. ", etc.)
    if re.match(r"^\d+\.\s", lines[0]) and all(re.match(r"^\d+\.\s", l) or l.startswith("  ") or l == "" for l in lines):
        items_ol: list[str] = []
        for line in lines:
            stripped = line.strip()
            ol_match = re.match(r"^\d+\.\s+(.*)", stripped)
            if ol_match:
                items_ol.append(md_inline(ol_match.group(1)))
            elif stripped and items_ol:
                items_ol[-1] += f"<br>{md_inline(stripped)}"
        html = "<ol>\n"
        for item in items_ol:
            html += f"<li>{item}</li>\n"
        html += "</ol>\n"
        return html

    # Default: paragraph
    return f"<p>{md_inline(block)}</p>\n"


