# Exact type replacements
_DISPLAY_TYPE_MAP = {
    "NotUndefined": "var",
    "undefined": None,       # None = omit when used as return type
    "void": None,
    "int64": "int",
    "Integer": "int",
    "float": "double",
    "Double": "double",
    "Number": "double",
    "DynamicObject *": "Object",
    "MainController *": "var",
    "NodeBase::Holder *": "var",
    "JSON": "var",
    "ScriptObject": "Object",
}


def display_type(raw_type: str):
    """Map a raw C++ type to a HISEScript-idiomatic display type.

    Returns None for void-like types (caller should omit the return type).
    Returns the cleaned string for everything else.
    """
    t = raw_type.strip()

    # Exact match first
    if t in _DISPLAY_TYPE_MAP:
        return _DISPLAY_TYPE_MAP[t]

    # ScriptingObjects::X * -> X
    if t.startswith("ScriptingObjects::"):
        t = t.replace("ScriptingObjects::", "")
        return t.rstrip(" *")

    # Other pointer types: strip trailing " *"
    if t.endswith(" *"):
        return t[:-2]

    # ScriptingSamplerSound *, ScriptingSynth * (no namespace prefix)
    if t.endswith("*"):
        return t.rstrip("*").rstrip()

    return t


def format_signature(class_name: str, method_name: str, m: dict) -> str:
    """Build a HISEScript-idiomatic signature for HTML display.

    Format: [ReturnType ]ClassName.methodName(Type param, ...)
    Return type is omitted when void/undefined.
    """
    # Return type
    ret = display_type(m.get("returnType", "undefined"))

    # Parameters
    param_strs = []
    for p in m.get("parameters", []):
        pt = display_type(p.get("type", "var"))
        if pt is None:
            pt = "var"
        param_strs.append(f"{pt} {p.get('name', '')}")

    sig = f"{class_name}.{method_name}({', '.join(param_strs)})"
    if ret is not None:
        sig = f"{ret} {sig}"
    return sig


def render_llm_ref(text: str) -> str:
    """Render Phase 4b LLM C++ reference text as styled HTML.

    The input is semi-structured plain text with known section labels.
    Returns HTML with coloured labels and thread-safety indicators.
    """
    lines = text.split("\n")
    out = []

    # Known section label prefixes (rendered as amber bold)
    _SECTION_LABELS = (
        "Thread safety:",
        "Dispatch/mechanics:",
        "Pair with:",
        "Anti-patterns:",
        "Source:",
        "Required setup:",
    )

    for line in lines:
        esc = html_escape(line)

        # Skip the first line (signature) — it's rendered separately as h3
        # Detect: "ClassName::methodName(...)" pattern on the very first line
        if not out and "::" in line and "(" in line:
            continue

        # Thread safety line
        if esc.startswith("Thread safety:"):
            label = "Thread safety:"
            rest = esc[len(label):]
            if "UNSAFE" in rest:
                rest = rest.replace("UNSAFE",
                    '<span class="llm-unsafe">UNSAFE</span>', 1)
            elif "SAFE" in rest:
                rest = rest.replace("SAFE",
                    '<span class="llm-safe">SAFE</span>', 1)
            out.append(f'<span class="llm-label">{label}</span>{rest}')
            continue

        # Source lines (muted)
        if esc.lstrip().startswith("ScriptingApi.cpp:") or \
           esc.lstrip().startswith("ScriptingObjects.cpp:"):
            out.append(f'<span class="llm-source">{esc}</span>')
            continue

        # Section labels
        matched = False
        for label in _SECTION_LABELS:
            elabel = html_escape(label)
            if esc.startswith(elabel) or esc.lstrip().startswith(elabel):
                indent = esc[:len(esc) - len(esc.lstrip())]
                rest = esc.lstrip()[len(elabel):]
                out.append(f'{indent}<span class="llm-label">{elabel}</span>{rest}')
                matched = True
                break
            # Also match when label appears after indentation
        if not matched:
            out.append(esc)

    return "\n".join(out)


def parse_decision_log(class_name: str) -> dict:
    """Parse decision log markdown into structured data.
    
    Returns:
        {
            "class_decisions": ["decision 1", "decision 2", ...],
            "method_decisions": {
                "methodName": ["decision 1", "decision 2", ...],
                ...
            }
        }
    """
    decisions_file = OUTPUT_DIR / "decisions" / f"{class_name}_phase4a.md"
    
    if not decisions_file.is_file():
        return {"class_decisions": [], "method_decisions": {}}
    
    with open(decisions_file, "r", encoding="utf-8") as f:
        content = f.read()
    
    result = {"class_decisions": [], "method_decisions": {}}
    
    # Split into sections by ## headings
    sections = re.split(r'^## (.+)$', content, flags=re.MULTILINE)
    
    current_section = None
    for i, part in enumerate(sections):
        if i % 2 == 1:  # Heading
            current_section = part.strip()
        elif i % 2 == 0 and current_section:  # Content
            if "class-level" in current_section.lower():
                # Extract bullet points
                for line in part.strip().split('\n'):
                    line = line.strip()
                    if line.startswith('- '):
                        result["class_decisions"].append(line[2:])
            elif "method" in current_section.lower():
                # Split by ### method headings
                method_parts = re.split(r'^### (.+)$', part, flags=re.MULTILINE)
                current_method = None
                for j, mpart in enumerate(method_parts):
                    if j % 2 == 1:  # Method name
                        current_method = mpart.strip()
                        result["method_decisions"][current_method] = []
                    elif j % 2 == 0 and current_method:  # Method decisions
                        for line in mpart.strip().split('\n'):
                            line = line.strip()
                            if line.startswith('- '):
                                result["method_decisions"][current_method].append(line[2:])
    
    return result


